package entidades;

public class Loja {

	// Atributos
	private String nome;
	private String cnpj;

	// Construtores
	public Loja(String nome, String cnpj) {
		super();
		this.nome = nome;
		this.cnpj = cnpj;
	}

	// Encapsulamento
	public String getNome() {
		return nome;
	}

	public String getCnpj() {
		return cnpj;
	}

	// Métodos


	
}